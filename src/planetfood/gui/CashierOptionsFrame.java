        if(jrbTakeOrder.isSelected())
        {
            TakeOrderPanel top=new TakeOrderPanel();
            top.setVisible(true);
            this.dispose();
        }
        if(jrbViewOrder.isSelected())
        {
            ViewOrders vo=new ViewOrders();
            vo.setVisible(true);
            this.dispose();
        }
        
